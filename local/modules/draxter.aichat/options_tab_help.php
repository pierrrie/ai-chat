function draxter_aichat_tab_help(string $tabId): void
{
    $tabs = draxter_aichat_help_tabs();
    if (!isset($tabs[$tabId])) {
        return;
    }

    $tab = $tabs[$tabId];
    echo '<tr><td colspan="2"><div class="draxter-admin-help">';
    echo '<div class="draxter-admin-help-title">' . htmlspecialcharsbx($tab['title']) . '</div>';
    echo '<div class="draxter-aichat-help">';
    echo $tab['html'];
    echo '</div></div></td></tr>';
}
